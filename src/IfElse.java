public class IfElse {
    public static void main(String[]args){
        int numero1 = 10;

        int numero2 = 20;

        if (numero1 > numero2){
            System.out.println("Numero 1 é maior que o numero 2");
        } else if (numero1 == numero2){
            System.out.println("numero 1 é igual ao 2");
        } else {
            System.out.println("Numero 2 que é o maior");
        }
    }
}
